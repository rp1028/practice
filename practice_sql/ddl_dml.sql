SELECT COUNT(*) AS TEAM_MEMBER_COUNT
FROM (
    -- 기술지원부의 대리
    SELECT M.MEMBER_CODE
    FROM MEMBER_INFO M
    JOIN TEAM_INFO T ON M.TEAM_CODE = T.TEAM_CODE
    WHERE T.TEAM_NAME = '기술지원부' AND M.POSITION = '대리'
    
    UNION ALL
    
    -- 인사관리부의 사원
    SELECT M.MEMBER_CODE
    FROM MEMBER_INFO M
    JOIN TEAM_INFO T ON M.TEAM_CODE = T.TEAM_CODE
    WHERE T.TEAM_NAME = '인사관리부' AND M.POSITION = '사원'
    
    UNION ALL
    
    -- 영업부(영업 포함)의 부장
    SELECT M.MEMBER_CODE
    FROM MEMBER_INFO M
    JOIN TEAM_INFO T ON M.TEAM_CODE = T.TEAM_CODE
    WHERE T.TEAM_NAME LIKE '%영업%' AND M.POSITION = '부장'
) AS SUB;  